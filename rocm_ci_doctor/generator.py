def _file_manifest(output_dir: Path, relative_path: str, content: str) -> dict[str, Any]:
    return {
        "path": relative_path,
        "absolute_path": str((output_dir / relative_path).resolve()),
        "bytes": len(content.encode("utf-8")),
        "sha256": hashlib.sha256(content.encode("utf-8")).hexdigest(),
        "preview": "\n".join(content.splitlines()[:8]),
    }
